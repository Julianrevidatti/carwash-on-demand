    "                                  }\n",
    "                               }\n",
    "                            }\n",
    "\n",
    "                            let totalDetectedDiscount = 0;\n",
    "                            supplierPromoDiscount.forEach(d => totalDetectedDiscount += d);\n",
    "\n",
    "                            // Attribute to selected supplier\n",
    "                            const baseDetectedForSupplier = supplierPromoDiscount.get(supplierFilter) || 0;\n",
    "                            let finalSupplierDiscount = baseDetectedForSupplier;\n",
