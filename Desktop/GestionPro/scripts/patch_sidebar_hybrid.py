    "                                     unitsUsed.forEach(u => { if (cartMap[u.id] > 0) cartMap[u.id]--; });\n",
    "\n",
    "                                     const discount = regularPriceSum - (combosPossible * promoPrice);\n",
    "                                     if (discount > 0) {\n",
    "                                        const supId = pidToSupplier[unitsUsed[0]?.id] || 'Desconocido';\n",
    "                                        supplierPromoDiscount.set(supId, (supplierPromoDiscount.get(supId) || 0) + discount);\n",
    "                                     }\n",
    "                                  }\n",
    "                               } else if (promoType === 'weighted' && Array.isArray(reqs) && reqs.length > 0) {\n",
    "                                  let possibleCombos = Infinity;\n",
    "                                  for (const req of reqs) {\n",
    "                                     const minWeight = Number(req.minWeight || 0);\n",
    "                                     const pid = req.productId || req.product_id;\n",
    "                                     const availableWeight = cartMap[pid] || 0;\n",
    "                                     if (availableWeight < minWeight) { possibleCombos = 0; break; }\n",
    "                                     const limit = Math.floor(availableWeight / minWeight);\n",
    "                                     if (limit < possibleCombos) possibleCombos = limit;\n",
    "                                  }\n",
    "                                  if (possibleCombos === Infinity) possibleCombos = 0;\n",
